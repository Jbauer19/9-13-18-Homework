public class VariableClass 
{

	public static void main(String[] args) 
	{
		String name = "Josh";
		System.out.println(name);
		
		int length = 5;
		int width = 2;
		int height = 10;
		int rectangleArea = length*width*height;
		System.out.println(rectangleArea);
		
		double pie = 3.14;
		int radius = 10;
		
		int circleArea = (int) (radius*radius*pie);
		System.out.println(circleArea);
		
		int circumphrence = (int) (2*pie*radius); 
		System.out.println(circumphrence);
	}


	
	
}
